student = {
    'name': 'Alice',
    'age': 21,
    'course': 'Python'
}
print("Dictionary:", student)

print("student name:",student['name'])

# Add or update value
student['grade'] = 'A'
print("After adding grade:", student)

# Remove a key
del student['age']
print("After deleting age:", student)

# Loop through dictionary
for key, value in student.items():
    print(f"{key}: {value}")

