# 🔹 1. LISTS
fruits = ['apple', 'banana', 'cherry', 'apple']
print("Original list:", fruits)
print("First fruit:", fruits[0])

fruits.append('kiwi')
print("after appending:",fruits)

fruits.remove('apple')
print("after removing:",fruits)

print("number of fruits:",len(fruits))

